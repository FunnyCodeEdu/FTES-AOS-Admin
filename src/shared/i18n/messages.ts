export function getMessage(lang: SupportedLang, key: string, params?: Record<string, string>): string {
  let text = messages[lang][key] ?? messages.vi[key] ?? key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, v);
    });
  }
  return text;
}
